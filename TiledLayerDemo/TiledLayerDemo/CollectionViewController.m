static NSString * const reuseIdentifier = @"Cell";

- (void)viewDidLoad {
    [super viewDidLoad];
    
    NSString *filePath = [[NSBundle mainBundle] pathForResource:@"Imgs" ofType:@"plist"];
    self.imags = [NSArray arrayWithContentsOfFile:filePath];
}

